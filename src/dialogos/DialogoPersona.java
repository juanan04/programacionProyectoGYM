    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        /*Condicional para realizar ciertas líneas según la acción*/
        if (tipo != null) {
            if (tipo.equals("Añadir")) { // Lineas para la acción Añadir
                Persona nueva = new Persona();
                nueva.setNombre(textNombre.getText());
                nueva.setApellidos(textApellidos.getText());
                if (textDni.getText().length() == 9) {
                    nueva.setDni(textDni.getText());
                }
                nueva.setFechaNacimiento(LocalDate.parse(textFecha.getText(), formatoFecha));
                nueva.setSexo(Sexo.valueOf(((String) comboSexo.getSelectedItem()).toUpperCase()));
                gym.getPersonas().add(nueva);
                pagPri.getModeloLista().addElement(nueva);
                JOptionPane.showMessageDialog(
                        this,
                        "La persona ha sido añadida con éxito",
                        "Cambio Realizado",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else if (tipo.equals("Actualizar")) { //Lineas para la acciónActualizar
                Persona actualizarPersona = pagPri.getSelectedPersonaListaDatos();
                actualizarPersona.setNombre(textNombre.getText());
                actualizarPersona.setApellidos(textApellidos.getText());
                if (textDni.getText().length() == 9) {
                    actualizarPersona.setDni(textDni.getText());
                } else {
                    JOptionPane.showMessageDialog(
                            this, 
                            "El formato tiene que ser '00000000A' y no mayor a 9", 
                            "Formato Erróneo", 
                            JOptionPane.ERROR_MESSAGE
                    );
                    this.dispose();
                }
                actualizarPersona.setFechaNacimiento(LocalDate.parse(textFecha.getText(), formatoFecha));
                actualizarPersona.setSexo(Sexo.valueOf(((String) comboSexo.getSelectedItem()).toUpperCase()));
                JOptionPane.showMessageDialog(
                        this,
                        "Se ha actualizado a la persona con éxito",
                        "Cambio Realizado",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }
        }
        dispose();
    }//GEN-LAST:event_botonAceptarActionPerformed

