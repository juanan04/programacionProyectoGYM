    // Método para indicar al dialogo si la acción es una nueva persona o actualización de una ya existente
    public void tipoAccion(String tipo) {
        this.tipo = tipo; //Inicializamos el tipo de String recibido
        txtTitulo.setText(tipo); // Establecemos el texto de inicio (Actualizar o Añadir)
        botonAceptar.setText(tipo); //Hacemos lo mismo que con el título pero para el JButton

        //Condicional para que introduzca directamente en los campos de texto los datos de la persona seleccionada
        if (tipo.equals("Actualizar")) {
            Persona actualizarPersona = pagPri.getSelectedPersonaListaDatos();
            textNombre.setText(actualizarPersona.getNombre()); // Mostramos Nombre en el JFieldText
            textApellidos.setText(actualizarPersona.getApellidos()); //Mostramos Apellidos en el JFieldText
            textDni.setText(actualizarPersona.getDni()); //Mostramos DNI en el JFieldText
            textFecha.setText(actualizarPersona.getFechaNacimiento().format(formatoFecha)); // Mostramos la fecha en el JFieldText
            comboSexo.setSelectedItem(actualizarPersona.getSexo());
        }
    }

